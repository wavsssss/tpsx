// Load fan sites on fansites.html page
if (window.location.pathname.includes('fansites.html')) {
    document.addEventListener('DOMContentLoaded', function() {
        const urlParams = new URLSearchParams(window.location.search);
        const member = urlParams.get('member');

        if (member && memberNames[member]) {
            const memberName = memberNames[member];
            const sNumber = memberToSNumber[member];
            const fansites = membersData[sNumber];
            const memberColor = memberColors[sNumber];
            const memberPhoto = memberPhotos[sNumber];

            // Update member header
            document.getElementById('memberName').textContent = memberName;
            document.getElementById('memberNumber').textContent = sNumber;

            // Update member icon with photo and colored border
            const memberIcon = document.getElementById('memberIcon');
            memberIcon.style.backgroundImage = `url('${memberPhoto}')`;
            memberIcon.style.backgroundSize = 'cover';
            memberIcon.style.backgroundPosition = 'center';
            memberIcon.style.border = `6px solid ${memberColor}`;

            // Display fan sites
            const fansitesList = document.getElementById('fansitesList');

            if (fansites && fansites.length > 0) {
                fansites.forEach(fansite => {
                    const card = document.createElement('a');
                    card.className = 'fansite-card';
                    card.href = `https://twitter.com/${fansite.handle.replace('@', '')}`;
                    card.target = '_blank';
                    card.rel = 'noopener noreferrer';

                    card.innerHTML = `
                        <div class="fansite-info">
                            <div class="fansite-name">${fansite.name}</div>
                            <div class="fansite-handle">${fansite.handle}</div>
                        </div>
                        <div class="fansite-arrow">â†’</div>
                    `;

                    fansitesList.appendChild(card);
                });
            } else {
                fansitesList.innerHTML = '<p style="text-align: center; color: #999;">No fan sites available yet.</p>';
            }
        }
    });
}

// Search function for index page
function filterMembers() {
    const input = document.getElementById('searchInput');
    const filter = input.value.toUpperCase();
    const grid = document.getElementById('membersGrid');
    const members = grid.getElementsByClassName('member-icon');

    for (let i = 0; i < members.length; i++) {
        const memberName = members[i].getAttribute('data-name');
        if (memberName.toUpperCase().indexOf(filter) > -1) {
            members[i].style.display = '';
        } else {
            members[i].style.display = 'none';
        }
    }
}
